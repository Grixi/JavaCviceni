public class Operace {
    public static void main (String[] args) {
        float x;
        float y;
        float z;

        x = 10;
        y = 3;
        z = x-y;
        System.out.println ("odčítání: "+z);
        z = x+y;
        System.out.println ("sčítání: "+z);
        z = x/y;
        System.out.println ("dělení: "+z);
        z = x*y;
        System.out.println ("násobení: "+z);
        z = x%y;
        System.out.println ("modulo: "+z);
    }
}